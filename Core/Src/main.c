// ======== BUTTON CALIBRATION HANDLING ========
uint32_t button_down_time = 0;
bool button_waiting_for_release = false;

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    if (GPIO_Pin == B1_Pin)
    {
        // First edge = button pressed
        if (!button_waiting_for_release)
        {
            button_waiting_for_release = true;
            button_down_time = HAL_GetTick();
        }
        // Second edge = button released
        else
        {
            uint32_t held = HAL_GetTick() - button_down_time;
            button_waiting_for_release = false;

            if (held < 1000)
            {
                Debug_Print("BUTTON: Short press → Calibrate WET\r\n");
                Moisture_Calibrate_Wet();
            }
            else
            {
                Debug_Print("BUTTON: Long press → Calibrate DRY\r\n");
                Moisture_Calibrate_Dry();
            }
        }
    }
}

